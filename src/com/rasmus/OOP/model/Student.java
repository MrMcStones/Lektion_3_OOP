package com.rasmus.OOP.model;

public class Student {

    public String name;
    public int age;
    public boolean isTired;

    // Constructor
    // Value = instantiation param
    public Student(String name, int age, boolean isTired) {
        this.name = name;
        this.age = age;
        this.isTired = isTired;
    }
}
